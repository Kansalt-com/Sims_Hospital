def _build_invoice_pdf(invoice: Dict[str, str], hospital_name: str) -> bytes:
    buffer = BytesIO()
    pdf = canvas.Canvas(buffer, pagesize=A4)
    width, height = A4

    pdf.setFont("Helvetica-Bold", 16)
    pdf.drawString(45, height - 55, "SIMS Hospital Cloud")
    pdf.setFont("Helvetica", 11)
    pdf.drawString(45, height - 74, hospital_name)

    pdf.setFont("Helvetica-Bold", 12)
    pdf.drawRightString(width - 45, height - 55, f"Invoice {invoice['invoice_number']}")
    pdf.setFont("Helvetica", 10)
    pdf.drawRightString(width - 45, height - 72, f"Issued: {invoice['issued_on']}")
    pdf.drawRightString(width - 45, height - 88, f"Due: {invoice['due_date']}")

    y = height - 130
    details = [
        f"Patient: {invoice['patient_name']}",
        f"Phone: {invoice['patient_phone']}",
        f"Address: {invoice['patient_address']}",
        f"Diagnosis: {invoice['patient_diagnosis']}",
        f"Doctor Assigned: {invoice['patient_doctor']}",
    ]
    pdf.setFont("Helvetica", 11)
    for line in details:
        pdf.drawString(45, y, line)
        y -= 18

    y -= 16
    pdf.setFont("Helvetica-Bold", 11)
    pdf.drawString(45, y, "Description")
    pdf.drawString(320, y, "Status")
    pdf.drawRightString(width - 45, y, "Amount")
    y -= 14
    pdf.line(45, y, width - 45, y)
    y -= 20

    pdf.setFont("Helvetica", 10)
    pdf.drawString(45, y, "Clinical Services and Consultation")
    pdf.drawString(320, y, str(invoice["status"]))
    pdf.drawRightString(width - 45, y, format_currency(float(invoice["amount"])))

    y -= 32
    pdf.setFont("Helvetica-Bold", 12)
    pdf.drawRightString(width - 45, y, f"Total: {format_currency(float(invoice['amount']))}")

    pdf.setFont("Helvetica-Oblique", 9)
    pdf.drawString(
        45,
        40,
        "Auto-generated by SIMS Hospital Cloud demo. Production deployments support enterprise audit trails.",
    )
    pdf.save()
    buffer.seek(0)
    return buffer.read()


