def render_billing_page(context: Dict[str, str]) -> None:
    st.markdown("## Billing Module")
    st.caption(f"{context['hospital_name']} | Auto-generated invoices and PDF export")

    patients = fetch_patients()
    if not patients:
        st.info("No patient records available for billing.")
        return

    labels = [f"#{patient['id']} - {patient['name']}" for patient in patients]
    selected_label = st.selectbox("Select Patient for Billing", labels)
    selected_patient = patients[labels.index(selected_label)]

    summary_left, summary_right = st.columns(2, gap="medium")
    with summary_left:
        st.markdown(
            f"""
            <div class="insight-card">
                <h4>Billing Amount</h4>
                <p>{format_currency(float(selected_patient['billing_amount']))}</p>
            </div>
            """,
            unsafe_allow_html=True,
        )
    with summary_right:
        st.markdown(
            f"""
            <div class="insight-card">
                <h4>Payment Status</h4>
                <p>{selected_patient['payment_status']}</p>
            </div>
            """,
            unsafe_allow_html=True,
        )

    if st.button("Auto Generate Invoice", type="primary"):
        invoice = create_invoice(int(selected_patient["id"]), context["username"])
        st.session_state.latest_invoice_id = invoice["id"]
        st.success(f"Invoice {invoice['invoice_number']} generated successfully.")

    invoice_id = st.session_state.get("latest_invoice_id")
    invoice = get_invoice(invoice_id) if invoice_id else None

    if invoice:
        st.markdown("### Invoice Preview")
        preview_html = _invoice_preview_html(invoice, context["hospital_name"])
        st.markdown(preview_html, unsafe_allow_html=True)

        download_col, print_col = st.columns(2, gap="small")
        with download_col:
            pdf_data = _build_invoice_pdf(invoice, context["hospital_name"])
            st.download_button(
                "Download Invoice as PDF",
                data=pdf_data,
                file_name=f"{invoice['invoice_number']}.pdf",
                mime="application/pdf",
                use_container_width=True,
            )
        with print_col:
            if st.button("Open Print Friendly Invoice", use_container_width=True):
                components.html(_print_frame(preview_html), height=640, scrolling=True)

    st.markdown("### Recent Invoices")
    recent = list_recent_invoices()
    if not recent:
        st.info("No invoices generated yet.")
        return
    table = pd.DataFrame(recent)
    table.columns = ["ID", "Invoice Number", "Amount", "Status", "Issued On", "Patient Name"]
    table["Amount"] = table["Amount"].apply(lambda amount: format_currency(float(amount)))
    st.dataframe(table, use_container_width=True, hide_index=True)
